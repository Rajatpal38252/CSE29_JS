// Primitive types

var num = 10;
console.log(num);   // 10
console.log(typeof num);  // number

var num = "Hello";
console.log(num);
console.log(typeof num); //string

var num = true;
console.log(num);
console.log(typeof num);  // boolean

var num = null;
console.log(num);
console.log(typeof num); // null

var num = undefined;
console.log(num);
console.log(typeof num); //undefined